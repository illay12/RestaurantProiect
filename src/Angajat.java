public class Angajat {

private String nume;
private String prenume;
private String rol;

public Angajat(String nume,String prenume,String rol)
{
    this.nume = nume;
    this.prenume = prenume;
    this.rol = rol;
}

public String getNume()
{
    return nume;
}

public String getPrenume()
{
    return prenume;
}

public String getRol()
{
    return rol;
}

}
